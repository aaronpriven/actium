} ## tidy end: sub _ldh_header

sub line_classification {

    my $line = shift;

    return 'Transbay' if $line eq '800' or $line =~ /\A[A-Z]/;
    return 'Supplementary' if $line =~ /\A6\d\d/;
    return 'All Nighter'   if $line =~ /\A8\d\d/;
    return 'Local';

}

1;


sub printheaders {

   print OUT "<table border=1>\n<tr><th>" , join ("</th><th>" , 
     "Stop ID" , "City" , "Neighborhood" , "Location" ,
       "Sign Type" , "Notes" , "Date Installed" , "Project" );
   print OUT "</th></tr>\n";


}
